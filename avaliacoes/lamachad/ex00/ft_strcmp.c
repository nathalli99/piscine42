int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
		{
			return (s1[i] - s2[i]);
		}
		i++;
	}
	return (0);
}

#include <stdio.h>

int	main()
{
	char	*s3 = "zebra";
	char	*s4 = "zebrb";

	printf("%d\n", ft_strcmp(s3, s4));
}
