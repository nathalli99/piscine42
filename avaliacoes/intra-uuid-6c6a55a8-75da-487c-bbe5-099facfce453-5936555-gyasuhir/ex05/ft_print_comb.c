#include	<unistd.h>

void	ft_print_comb(void)
{
	char	d_1;
	char	d_2;
	char	d_3;

	d_1 = '0';
	while (d_1 <= '7')
	{
		d_2 = d_1 + 1;
		while (d_2 <= '8')
		{
			d_3 = d_2 + 1;
			while (d_3 <= '9')
			{
				write(1, &d_1, 1);
				write(1, &d_2, 1);
				write(1, &d_3, 1);
				if (d_1 != '7')
					write(1, ", ", 2);
				d_3++;
			}
			d_2++;
		}
		d_1++;
	}
	return ;
}

int     main(void)
{       
        ft_print_comb();
}   
