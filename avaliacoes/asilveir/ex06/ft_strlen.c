#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	int	number_of_chars;

		number_of_chars = ft_strlen("dois");
	printf("Number of chars: %d", number_of_chars);
	return (0);
}
